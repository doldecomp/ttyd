WorldData* worldDataPtr(void);
int maxMapInArea(const char* areaName);
int maxAreaInWorld(void);

MapData* mapDataPtr(const char* mapName) {
    WorldData* wp = worldDataPtr();
    int i;
    int j;

    for (i = 0; i < maxAreaInWorld(); i++) {
        for (j = 0; j < maxMapInArea(wp->areas[i]->areaName); j++) {
            MapData* map = wp->areas[i]->maps[j];
            if (!strcmp(map->mapName, mapName)) {
                return map;
            }
        }
    }
    return NULL;
}

AreaData* areaDataPtr(const char* areaName) {
    WorldData* wp = worldDataPtr();
    int max = maxAreaInWorld();
    int i;

    for (i = 0; i < max; i++) {
        if (!strcmp(wp->areas[i]->areaName, areaName)) {
            return wp->areas[i];
        }
    }
    return NULL;
}

WorldData* worldDataPtr(void) {
    return &world_data;
}

int maxMapInArea(const char* areaName) {
    AreaData* ap = areaDataPtr(areaName);
    int n = 0;

    while (ap->maps[n] != NULL) {
        n++;
    }
    return n;
}

int maxAreaInWorld(void) {
    WorldData* wp = worldDataPtr();
    int n = 0;

    while (wp->areas[n] != NULL) {
        n++;
    }
    return n;
}

void relSetEvtAddr(const char* mapName, s32* eventAddr) {
    MapData* map = mapDataPtr(mapName);
    if (map != NULL) {
        map->eventAddr = eventAddr;
    }
}

void relSetBtlAddr(const char* areaName, void* field_8, void* field_C) {
    AreaData* area = areaDataPtr(areaName);
    if (area != NULL) {
        area->field_8 = field_8;
        area->field_C = field_C;
    }
}
